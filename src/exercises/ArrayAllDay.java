package exercises;

// Create and initialize an array with the following values in a single line:
// 1, 1, 2, 3, 5, 8. Then loop through the array and print out each value.

public class ArrayAllDay {
    public static void main(String[] args){

        String nums[] = {"1", "1", "2", "3", "5", "8"};
        for (String num:nums){
            System.out.println(num);
        }
    }
}
